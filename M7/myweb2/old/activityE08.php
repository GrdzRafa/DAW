						<div class="practica-desc">
							<p>Una contrasenya, clau d'accés o mot de pas (en algun programari apareix denominada com a password provinent de l'anglès), és una forma d'autentificació que utilitza informació secreta per controlar l'accés a un recurs. La contrasenya normalment ha de mantenir-se en secret davant d'aquells a qui no es permet l'accés. L'ordinador sol·licita una clau a qui vulgui accedir i només es podrà accedir si es coneix.</p>
							<p>Tècniques comunes utilitzades per millorar la seguretat de sistemes de programari protegides per contrasenyes inclouen:</p>
							<p>No repetir la contrasenya a la pantalla de visualització quan s'està accedint.</p>
							<p>Permetre contrasenyes d'una longitud adequada (alguns sistemes d'Unix limiten contrasenyes a 8 caràcters)</p>
							<p>Obligar que la contrasenya tingui algun caràcter especial i algun nombre</p>
							<p>Requerir als usuaris tornar a ingressar la contrasenya després d'un període d'inactivitat.</p>
							<p>Fer complir una política de contrasenyes per assegurar contrasenyes importants.</p>
							<p>Requerir periòdicament canvis de contrasenya.</p>
							<p>Assignar contrasenyes l'atzar.</p>
							<p>Proveir una opció alternativa a l'ús de teclats.</p>
							<p>Dissenya una funció que verifiqui la complexitat d'una contrasenya, seguint les recomanacions de la <a href="https://www.ontek.net/test-contrasenas/">pàgina OnTek -  Test de complejidad de las contraseñas.</a></p>
